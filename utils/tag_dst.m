function tag = tag_dst(time)

    summer = ['2000-04-02'; '2001-04-01'; '2002-04-07'; '2003-04-06'; '2004-04-04'; '2005-04-03'; '2006-04-02'; '2007-03-11'; '2008-03-09'; '2009-03-08'; '2010-03-14'; '2011-03-13'; '2012-03-11'; '2013-03-10'; '2014-03-09'];
    winter = ['2000-10-29'; '2001-10-28'; '2002-10-27'; '2003-10-26'; '2004-10-31'; '2005-10-30'; '2006-10-29'; '2007-11-04'; '2008-11-02'; '2009-11-01'; '2010-11-07'; '2011-11-06'; '2012-11-04'; '2013-11-03'; '2014-11-02'];
    
    yy = arrayfun(@(Year) datenum(Year,2,1),2000:2014);
    
    s = datenum(summer)+2/24;
    w = datenum(winter)+2/24;

    gety = @(t) sum(yy <= t); 
    y = arrayfun(gety,time);

    tag = (time >= s(y)) & (time < w(y));

end